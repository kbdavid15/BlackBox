package edu.kettering.blackbox;

public class Vehicle {
	private String vMake, vModel;
	
	
	public Vehicle() {
		
	}
	
	public String getMake() {
		return vMake;
	}
	public void setMake(String vMake) {
		this.vMake = vMake;
	}
	public String getModel() {
		return vModel;
	}
	public void setModel(String vModel) {
		this.vModel = vModel;
	}
}
